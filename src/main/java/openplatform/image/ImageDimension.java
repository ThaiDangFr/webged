public class ImageDimension
{
    private int width;
    private int height;

    public int getHeight()
    {
        return height;
    }

    public void setHeight(int aheight)
    {
        height=aheight;
    }

    public int getWidth()
    {
        return width;
    }

    public void setWidth(int awidth)
    {
        width=awidth;
    }
    
}
